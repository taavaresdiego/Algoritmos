#include <iostream>

using namespace std;

int main() {
  // 30 + 30; --> soma
  // 30 - 30; --> subtração
  // 30 * 30; --> multiplicação
  // 30 / 30; --> divisão

  int soma = 30 + 30;
  int subtracao = 30 - 30;
  int multiplicacao = 30 * 30;
  int divisao = 30 / 30;

  cout << "Soma: " << soma << endl;
  cout << "Subtração: " << subtracao << endl;
  cout << "Multiplicação: " << multiplicacao << endl;
  cout << "Divisão: " << divisao << endl;
  
  return 0;
}

